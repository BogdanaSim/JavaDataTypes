public class Calculator {

    public int addition(int number1, int number2){
        return number1+number2;
    }

    public int addition(int number1, int number2, int number3){
        return number1+number2+number3;
    }

    public float addition(float number1, float number2){
        return number1+number2;
    }

    public float addition(float number1, float number2, float number3){
        return number1+number2+number3;
    }

    public int subtraction(int number1, int number2){
        return number1-number2;
    }

    public int subtraction(int number1, int number2, int number3){
        return number1-number2-number3;
    }

    public float subtraction(float number1, float number2){
        return number1-number2;
    }

    public float subtraction(float number1, float number2, float number3){
        return number1-number2-number3;
    }

    public int multiplication(int number1, int number2){
        return number1*number2;
    }

    public int multiplication(int number1, int number2, int number3){
        return number1*number2*number3;
    }

    public float multiplication(float number1, float number2){
        return number1*number2;
    }

    public float multiplication(float number1, float number2, float number3){
        return number1*number2*number3;
    }
    public float division(int number1, int number2){
        return (float)number1/number2;
    }

    public float division(int number1, int number2, int number3){
        return (float) number1/number2/number3;
    }

    public float division(float number1, float number2){
        return number1/number2;
    }

    public float division(float number1, float number2, float number3){
        return number1/number2/number3;
    }

}
